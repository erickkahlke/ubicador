// Scanner optimizado: GS1-128 + QR simple y confiable
class BarcodeScanner {
    constructor() {
        this.isScanning = false;
        this.integratedScanner = document.getElementById('integrated-scanner');
        this.scannerContainer = document.getElementById('integrated-scanner-container');
        this.closeIntegratedBtn = document.getElementById('close-integrated-scanner');
        this.stream = null;
        this.video = null;
        
        console.log('üîß BarcodeScanner inicializado:');
        console.log('   - integratedScanner:', !!this.integratedScanner);
        console.log('   - scannerContainer:', !!this.scannerContainer);
        console.log('   - closeIntegratedBtn:', !!this.closeIntegratedBtn);
        
        // QR Reader simple y confiable
        this.html5QrScanner = null;
        this.isCodeDetected = false;
        this.lastScanTime = 0; // Para debounce de escaneos
        
        // Detecci√≥n de c√≥digos duplicados
        this.lastCode = null;
        this.lastCodeTime = 0;
        this.DUPLICATE_TIMEOUT = 5000; // 5 segundos para considerar duplicado
        
        this.initializeEventListeners();
        this.initializeQR();
    }
    
    initializeQR() {
        if (typeof Html5Qrcode === 'undefined') {
            console.warn('Html5-QrCode no disponible');
        }
    }

    initializeEventListeners() {
        // Verificar que el bot√≥n existe
        if (this.closeIntegratedBtn) {
            console.log('‚úÖ Bot√≥n de cerrar scanner encontrado, agregando event listener...');
            
            // Cerrar scanner integrado - con m√∫ltiples eventos para asegurar captura
            this.closeIntegratedBtn.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                console.log('üî¥ CLICK EVENT - Bot√≥n X clickeado - cerrando scanner...');
                this.stopScanner();
            });
            
            // Backup con mousedown por si click no funciona
            this.closeIntegratedBtn.addEventListener('mousedown', (e) => {
                e.preventDefault();
                e.stopPropagation();
                console.log('üî¥ MOUSEDOWN EVENT - Bot√≥n X presionado - cerrando scanner...');
                this.stopScanner();
            });
            
            // Backup con touchstart para m√≥viles
            this.closeIntegratedBtn.addEventListener('touchstart', (e) => {
                e.preventDefault();
                e.stopPropagation();
                console.log('üî¥ TOUCHSTART EVENT - Bot√≥n X tocado - cerrando scanner...');
                this.stopScanner();
            });
            
            console.log('üì± Event listeners agregados correctamente al bot√≥n X');
        } else {
            console.error('‚ùå Bot√≥n de cerrar scanner NO encontrado!');
        }

        // Flash button removed - functionality not needed

        // Cerrar con ESC
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && this.isScanning) {
                console.log('‚å®Ô∏è ESC presionado - cerrando scanner...');
                this.stopScanner();
            }
        });
    }

    async startScanner() {
        console.log('üé• Iniciando scanner integrado...');
        
        if (this.isScanning) {
            console.log('Scanner ya est√° activo');
            return;
        }

        try {
            // Verificar soporte de c√°mara
            if (!navigator.mediaDevices || !navigator.mediaDevices.getUserMedia) {
                throw new Error('C√°mara no soportada en este dispositivo');
            }

            // Mostrar scanner integrado y ocultar input
            this.integratedScanner.classList.remove('d-none');
            document.body.classList.add('scanner-active');
            this.isScanning = true;
            this.isCodeDetected = false;

            console.log('üé• Scanner UI mostrado, modo scanner activo');
            console.log('üì± Clases body:', document.body.className);
            console.log('üîç Bot√≥n cerrar existe:', !!this.closeIntegratedBtn);
            
            // Debug de clicks en modo scanner
            this.debugClickHandler = (e) => {
                if (document.body.classList.contains('scanner-active')) {
                    console.log('üñ±Ô∏è Click detectado en modo scanner:');
                    console.log('   - Target:', e.target.tagName, e.target.className, e.target.id);
                    console.log('   - Es bot√≥n X?:', e.target.id === 'close-integrated-scanner');
                    console.log('   - Coordenadas:', e.clientX, e.clientY);
                    
                    // Detectar si el click deber√≠a ir al bot√≥n X
                    const btnX = document.getElementById('close-integrated-scanner');
                    if (btnX) {
                        const rect = btnX.getBoundingClientRect();
                        const clickInBtnArea = e.clientX >= rect.left && e.clientX <= rect.right && 
                                             e.clientY >= rect.top && e.clientY <= rect.bottom;
                        console.log('   - Click en √°rea del bot√≥n X?:', clickInBtnArea);
                        console.log('   - Rect bot√≥n X:', rect.left, rect.top, rect.right, rect.bottom);
                        
                        if (clickInBtnArea && e.target.id !== 'close-integrated-scanner') {
                            console.log('‚ùå PROBLEMA: Click en √°rea del bot√≥n X interceptado por otro elemento!');
                            console.log('   - Elemento interceptor:', e.target.tagName, e.target.className);
                        }
                    }
                }
            };
            document.addEventListener('click', this.debugClickHandler);
            
            // Verificaci√≥n adicional del bot√≥n despu√©s de mostrar el scanner
            setTimeout(() => {
                const btnCheck = document.getElementById('close-integrated-scanner');
                console.log('üîç Verificaci√≥n post-apertura - Bot√≥n encontrado:', !!btnCheck);
                if (btnCheck) {
                    console.log('üîç Bot√≥n visible:', btnCheck.offsetWidth > 0 && btnCheck.offsetHeight > 0);
                    console.log('üîç Bot√≥n classes:', btnCheck.className);
                    console.log('üîç Bot√≥n z-index:', window.getComputedStyle(btnCheck).zIndex);
                    console.log('üîç Bot√≥n pointer-events:', window.getComputedStyle(btnCheck).pointerEvents);
                    
                    // Debug de elementos que podr√≠an interferir
                    const statusContainer = document.querySelector('.status-container');
                    if (statusContainer) {
                        console.log('üìã Status-container z-index:', window.getComputedStyle(statusContainer).zIndex);
                        console.log('üìã Status-container pointer-events:', window.getComputedStyle(statusContainer).pointerEvents);
                        console.log('üìã Status-container top:', window.getComputedStyle(statusContainer).top);
                    }
                    
                    const scannerHeader = btnCheck.closest('.scanner-header');
                    if (scannerHeader) {
                        console.log('üé¨ Scanner-header z-index:', window.getComputedStyle(scannerHeader).zIndex);
                    }
                    
                    // FORZAR event listener directo como backup
                    console.log('üîß Agregando event listener DIRECTO al bot√≥n como backup...');
                    const self = this; // Guardar referencia del contexto
                    btnCheck.onclick = (e) => {
                        e.preventDefault();
                        e.stopPropagation();
                        console.log('üî¥ ONCLICK DIRECTO - Bot√≥n X clickeado - cerrando scanner...');
                        self.stopScanner();
                    };
                }
            }, 100);
            console.log('üì± Verificando librer√≠as disponibles...');
            console.log('Html5Qrcode:', typeof Html5Qrcode !== 'undefined');
            console.log('Quagga:', typeof Quagga !== 'undefined');

            // Usar Html5-QrCode como principal, Quagga como fallback
            if (typeof Html5Qrcode !== 'undefined') {
                await this.startHtml5Scanner();
            } else {
                console.log('üì± Usando Quagga como fallback...');
                await this.startQuaggaScanner();
            }

            // Flash button removed

            console.log('üé• Scanner integrado iniciado exitosamente');

        } catch (error) {
            console.error('‚ùå Error iniciando scanner:', error);
            this.handleScannerError(error);
        }
    }

    async startHtml5Scanner() {
        try {
            console.log('üì± Iniciando Html5-QrCode para detecci√≥n completa...');
            
            // Crear nueva instancia cada vez para evitar conflictos
            this.html5QrScanner = new Html5Qrcode("integrated-scanner-container");
            
            const config = {
                fps: 10, // Reducir FPS para mejor detecci√≥n
                qrbox: { width: 350, height: 200 }, // √Årea m√°s grande para c√≥digos de barras
                aspectRatio: 1.75, // Proporci√≥n m√°s cuadrada
                // Html5-QrCode detecta autom√°ticamente QR y c√≥digos de barras
                experimentalFeatures: {
                    useBarCodeDetectorIfSupported: true
                },
                // Configuraci√≥n adicional para mejor detecci√≥n
                rememberLastUsedCamera: true,
                videoConstraints: {
                    facingMode: "environment",
                    focusMode: "continuous"
                }
            };

            await this.html5QrScanner.start(
                { facingMode: "environment" }, // c√°mara trasera
                config,
                (decodedText, decodedResult) => {
                    if (!this.isCodeDetected) {
                        console.log('üéØ C√≥digo detectado por Html5-QrCode:', decodedText);
                        console.log('Detalles:', decodedResult);
                        
                        // Determinar tipo de c√≥digo
                        let codeType = 'BARCODE';
                        if (decodedResult.result?.format) {
                            const format = decodedResult.result.format;
                            console.log('Formato detectado:', format);
                            if (format.toString().includes('QR')) {
                                codeType = 'QR';
                            } else if (this.isValidGS1128(decodedText)) {
                                codeType = 'GS1-128';
                            }
                        } else if (this.isValidGS1128(decodedText)) {
                            codeType = 'GS1-128';
                        }
                        
                        console.log(`‚úÖ ${codeType} detectado:`, decodedText);
                        this.isCodeDetected = true;
                        this.onCodeDetected(decodedText, codeType);
                    }
                },
                (errorMessage) => {
                    // Errores normales durante escaneo - no mostrar todos
                }
            );
            
            console.log('‚úÖ Html5-QrCode iniciado exitosamente');
            
        } catch (error) {
            console.error('‚ùå Error iniciando Html5-QrCode:', error);
            // Fallback a Quagga
            await this.startQuaggaScanner();
        }
    }

    async startQuaggaScanner() {
        console.log('üì± Iniciando Quagga como fallback...');
        
        const quaggaConfig = {
            inputStream: {
                name: "Live",
                type: "LiveStream",
                target: this.scannerContainer,
                constraints: {
                    width: { min: 640, ideal: 1280 },
                    height: { min: 480, ideal: 720 },
                    facingMode: "environment",
                    aspectRatio: { min: 1, max: 2 }
                },
                singleChannel: false
            },
            locator: {
                patchSize: "medium",
                halfSample: true
            },
            numOfWorkers: navigator.hardwareConcurrency || 4,
            frequency: 15,
            decoder: {
                readers: [
                    "code_128_reader", // Principal para GS1-128
                    "ean_reader",
                    "code_39_reader"
                ],
                multiple: false
            },
            locate: true
        };

        await new Promise((resolve, reject) => {
            Quagga.init(quaggaConfig, (err) => {
                if (err) {
                    console.error('‚ùå Error inicializando Quagga:', err);
                    reject(err);
                } else {
                    console.log('‚úÖ Quagga inicializado correctamente');
                    resolve();
                }
            });
        });

        Quagga.start();

        // Capturar video y stream para control de flash
        this.video = this.scannerContainer.querySelector('video');
        if (this.video && this.video.srcObject) {
            this.stream = this.video.srcObject;
            console.log('üìπ Stream capturado para control de flash');
        }

        // Event listener para c√≥digos de barras
        Quagga.onDetected((result) => {
            if (!this.isCodeDetected) {
                const code = result.codeResult.code;
                console.log('üì¶ C√≥digo detectado por Quagga:', code, 'Formato:', result.codeResult.format);
                
                const codeType = this.isValidGS1128(code) ? 'GS1-128' : 'BARCODE';
                console.log(`‚úÖ ${codeType} detectado:`, code);
                
                this.isCodeDetected = true;
                this.onCodeDetected(code, codeType);
            }
        });
    }
    
    // Validaci√≥n GS1-128
    isValidGS1128(code) {
        if (code.length >= 20 && /^\d+$/.test(code)) {
            return true;
        }
        if (code.match(/^(00|01|02|10|11|17|20|21|30|37|90|91|92|93|94|95|96|97|98|99)/)) {
            return true;
        }
        return false;
    }

    async stopScanner() {
        console.log('üõë Deteniendo scanner integrado...');
        
        if (!this.isScanning) {
            return;
        }

        try {
            // Detener Html5-QrCode si est√° activo
            if (this.html5QrScanner) {
                try {
                    await this.html5QrScanner.stop();
                    this.html5QrScanner.clear();
                } catch (e) {
                    console.log('Error deteniendo Html5-QrCode:', e);
                }
                this.html5QrScanner = null;
            }

            // Detener Quagga si est√° activo (siempre intentar detenerlo)
            if (typeof Quagga !== 'undefined') {
                try {
                    Quagga.stop();
                    Quagga.offDetected();
                    Quagga.offProcessed();
                    console.log('Quagga detenido');
                } catch (e) {
                    console.log('Error deteniendo Quagga:', e);
                }
            }

            // Ocultar scanner integrado y mostrar input
            console.log('üî¥ Cerrando scanner - ocultando UI...');
            this.integratedScanner.classList.add('d-none');
            document.body.classList.remove('scanner-active');
            this.isScanning = false;
            
            // Limpiar debug click handler
            if (this.debugClickHandler) {
                document.removeEventListener('click', this.debugClickHandler);
                this.debugClickHandler = null;
            }
            
            console.log('‚úÖ Modo scanner desactivado, clases body:', document.body.className);

            // Reiniciar variables
            this.video = null;
            this.isCodeDetected = false;
            this.lastScanTime = 0; // Reset debounce
            
            // Reset detecci√≥n de duplicados
            this.lastCode = null;
            this.lastCodeTime = 0;

            console.log('‚úÖ Scanner integrado detenido exitosamente');

        } catch (error) {
            console.error('‚ùå Error deteniendo scanner:', error);
        }

        // Reenfoque en input
        setTimeout(() => {
            document.getElementById('scanner-input').focus();
        }, 300);
    }

    onCodeDetected(code, type) {
        console.log(`üì¶ ${type} detectado en onCodeDetected:`, code);

        const currentTime = Date.now();

        // DEBOUNCE: Evitar escaneos repetidos en 1 segundo
        if (currentTime - this.lastScanTime < 1000) {
            console.log('‚è±Ô∏è Escaneo ignorado por debounce (muy reciente)');
            return;
        }

        // DETECCI√ìN DE DUPLICADOS: Evitar lecturas del mismo c√≥digo en 5 segundos
        if (this.lastCode === code && currentTime - this.lastCodeTime < this.DUPLICATE_TIMEOUT) {
            console.log(`üîÑ C√≥digo duplicado ignorado: "${code}" (le√≠do hace ${Math.round((currentTime - this.lastCodeTime) / 1000)} segundos)`);
            return;
        }

        // Actualizar registros de √∫ltimo c√≥digo y tiempo
        this.lastScanTime = currentTime;
        this.lastCode = code;
        this.lastCodeTime = currentTime;

        // Vibraci√≥n si est√° disponible
        if (navigator.vibrate) {
            navigator.vibrate(200);
        }

        // Sonido de √©xito
        try {
            const successSound = document.getElementById('success-sound');
            successSound.currentTime = 0;
            successSound.play().catch(e => {});
        } catch (e) {
            // Ignorar errores de audio
        }

        // Flash verde
        this.flashSuccess();

        // Mostrar mensaje temporal en el scanner
        this.showTemporaryMessage(`${type} detectado: ${code.substring(0, 25)}...`);

        // Procesar c√≥digo y permitir seguir escaneando
        if (typeof processScannedCode === 'function') {
            processScannedCode(code);
        }
        
        // Resetear flag para permitir detecci√≥n de siguiente c√≥digo
        setTimeout(() => {
            this.isCodeDetected = false;
        }, 1500);
    }

    drawBoundingBoxes(boxes) {
        // Opcional: dibujar cajas de detecci√≥n
        // QuaggaJS ya maneja esto internamente
    }

    // Flashlight functions removed - not working properly

    flashSuccess() {
        // Flash verde en toda la pantalla - M√ÅS NOTORIO
        const flashOverlay = document.getElementById('flash-overlay');
        if (flashOverlay) {
            flashOverlay.className = 'flash-overlay success show';
            setTimeout(() => {
                flashOverlay.classList.remove('show');
            }, 500); // Duraci√≥n m√°s larga para ser m√°s notorio
        }
    }

    handleScannerError(error) {
        console.error('Scanner error:', error);
        
        let message = 'Error al acceder a la c√°mara';
        
        if (error.name === 'NotAllowedError') {
            message = 'Permiso de c√°mara denegado. Por favor, habilite el acceso a la c√°mara.';
        } else if (error.name === 'NotFoundError') {
            message = 'No se encontr√≥ c√°mara en el dispositivo.';
        } else if (error.name === 'NotSupportedError') {
            message = 'C√°mara no soportada en este navegador.';
        }

        this.showError(message);
        this.stopScanner();
    }

    showError(message) {
        // Usar el sistema de mensajes existente
        if (typeof showError === 'function') {
            showError(message);
        } else {
            alert(message);
        }
    }

    showTemporaryMessage(message) {
        // Crear mensaje temporal en el scanner
        const messageDiv = document.createElement('div');
        messageDiv.style.cssText = `
            position: absolute;
            top: 10px;
            left: 50%;
            transform: translateX(-50%);
            background: rgba(0,0,0,0.8);
            color: white;
            padding: 10px 20px;
            border-radius: 20px;
            z-index: 1000;
            font-size: 14px;
        `;
        messageDiv.textContent = message;
        
        this.scannerContainer.appendChild(messageDiv);
        
        setTimeout(() => {
            if (messageDiv.parentNode) {
                messageDiv.parentNode.removeChild(messageDiv);
            }
        }, 3000);
    }
}

